/**
 * Send a JSON response and terminate execution
 * 
 * @param array $data     Data to send
 * @param int   $httpCode HTTP status code
 * 
 * @return void
 */
function sendJsonResponse($data, $httpCode = 200)
{
    // Clear any buffered output
    while (ob_get_level() > 0) {
        ob_end_clean();
    }
    
    http_response_code($httpCode);
    header('Content-Type: application/json; charset=utf-8');
    
    // Sanitize data to ensure valid JSON output
    $data = sanitizeForJson($data);
    
    $jsonFlags = JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE | JSON_UNESCAPED_SLASHES;
    
    // PHP 7.2+ supports JSON_INVALID_UTF8_SUBSTITUTE
    if (defined('JSON_INVALID_UTF8_SUBSTITUTE')) {
        $jsonFlags |= JSON_INVALID_UTF8_SUBSTITUTE;
    }
    
    echo json_encode($data, $jsonFlags);
    exit;
}

/**
 * Recursively sanitize data for JSON encoding
 * Removes invalid UTF-8 characters and control characters
 * 
 * @param mixed $data Data to sanitize
 * @return mixed Sanitized data
 */
function sanitizeForJson($data)
{
    if (is_array($data)) {
        // Re-index numeric arrays to avoid sparse arrays with null gaps
        $isNumeric = array_keys($data) === range(0, count($data) - 1);
        $result = [];
        foreach ($data as $key => $value) {
            $sanitizedValue = sanitizeForJson($value);
            if ($sanitizedValue !== null || !$isNumeric) {
                if ($isNumeric) {
                    $result[] = $sanitizedValue;
                } else {
                    $result[$key] = $sanitizedValue;
                }
            }
        }
        return $result;
    }
    
    if (is_object($data)) {
        $result = new \stdClass();
        foreach ($data as $key => $value) {
            $result->$key = sanitizeForJson($value);
        }
        return $result;
    }
    
    if (is_string($data)) {
        // Remove invalid UTF-8 sequences
        $data = mb_convert_encoding($data, 'UTF-8', 'UTF-8');
        // Remove control characters except newline, carriage return, tab
        $data = preg_replace('/[\x00-\x08\x0B\x0C\x0E-\x1F\x7F]/', '', $data);
        return $data;
    }
    
    return $data;
}

/**
 * Send an error response
 * 
 * @param int    $code    HTTP error code
 * @param string $message Error message
 * @param array  $extra   Additional data to include
 * 
 * @return void
 */
function sendErrorResponse($code, $message, $extra = [])
{
    $response = [
        'success' => false,
        'error'   => array_merge([
            'code'    => $code,
            'message' => $message,
        ], $extra),
    ];
    
    sendJsonResponse($response, $code);
}

/**
 * Get the real client IP address
 * 
 * Supports various proxy headers including Cloudflare.
 * 
 * @return string
 */
function getClientIp()
{
    $headers = [
        'HTTP_CF_CONNECTING_IP',     // Cloudflare
        'HTTP_X_FORWARDED_FOR',      // Generic proxies
        'HTTP_X_REAL_IP',            // Nginx proxy
        'HTTP_CLIENT_IP',            // Other proxies
        'REMOTE_ADDR',               // Direct connection
    ];
    
    foreach ($headers as $header) {
        if (!empty($_SERVER[$header])) {
            $ip = $_SERVER[$header];
            
            // X-Forwarded-For may contain multiple IPs, take the first one
            if (strpos($ip, ',') !== false) {
                $ips = explode(',', $ip);
                $ip = trim($ips[0]);
            }
            
            // Validate it's a valid IP address
            if (filter_var($ip, FILTER_VALIDATE_IP, FILTER_FLAG_NO_PRIV_RANGE | FILTER_FLAG_NO_RES_RANGE)) {
                return $ip;
            }
            
            // Accept private IPs for local development
            if (filter_var($ip, FILTER_VALIDATE_IP)) {
                return $ip;
            }
        }
    }
    
    return isset($_SERVER['REMOTE_ADDR']) ? $_SERVER['REMOTE_ADDR'] : '0.0.0.0';
}

/**
 * Get addon configuration setting value
