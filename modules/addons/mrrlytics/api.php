 * @param string $setting Setting name
 * 
 * @return string|null
 */
function getAddonSetting($setting)
{
    try {
        $result = Capsule::table('tbladdonmodules')
            ->where('module', 'mrrlytics')
            ->where('setting', $setting)
            ->value('value');
        
        return $result;
    } catch (\Exception $e) {
        return null;
    }
}

/**
 * Check rate limit for the current IP
 * 
 * Uses a sliding window algorithm with 1-minute windows.
 * 
 * @param string $ip          Client IP address
 * @param int    $maxRequests Maximum requests per minute
 * 
 * @return array ['allowed' => bool, 'remaining' => int, 'retry_after' => int]
 */
function checkRateLimit($ip, $maxRequests)
{
    $currentTime = time();
    $windowStart = $currentTime - 60; // 1-minute window
    
    try {
        // Clean up old records (older than 2 minutes)
        Capsule::table('mod_mrrlytics_ratelimit')
            ->where('request_time', '<', $currentTime - 120)
            ->delete();
        
        // Count requests in the current window
        $requestCount = Capsule::table('mod_mrrlytics_ratelimit')
            ->where('ip_address', $ip)
            ->where('request_time', '>=', $windowStart)
            ->count();
        
        if ($requestCount >= $maxRequests) {
            // Get the timestamp of the oldest request in the window
            $oldestRequest = Capsule::table('mod_mrrlytics_ratelimit')
                ->where('ip_address', $ip)
                ->where('request_time', '>=', $windowStart)
                ->orderBy('request_time', 'asc')
                ->value('request_time');
            
            $retryAfter = $oldestRequest ? ($oldestRequest + 60 - $currentTime) : 60;
            
            return [
                'allowed'     => false,
                'remaining'   => 0,
                'retry_after' => max(1, $retryAfter),
            ];
        }
        
        // Record this request
        Capsule::table('mod_mrrlytics_ratelimit')->insert([
            'ip_address'   => $ip,
            'request_time' => $currentTime,
        ]);
        
        return [
            'allowed'     => true,
            'remaining'   => $maxRequests - $requestCount - 1,
            'retry_after' => 0,
        ];
    } catch (\Exception $e) {
        // On error, allow the request (fail open)
        return [
            'allowed'     => true,
            'remaining'   => $maxRequests,
            'retry_after' => 0,
        ];
    }
}

/**
 * Extract the authentication header
 * 
 * Supports both Apache (getallheaders) and CGI/FastCGI (HTTP_ prefix).
 * 
 * @return string|null
 */
function getAuthHeader()
{
    $headers = [];
    
    // Apache and some other servers
    if (function_exists('getallheaders')) {
        $headers = getallheaders();
        if ($headers === false) {
            $headers = [];
        }
    }
    
    // Normalize keys to lowercase
    $headers = array_change_key_case($headers, CASE_LOWER);
    
    if (isset($headers['x-mrrlytics-key'])) {
        return $headers['x-mrrlytics-key'];
    }
    
    // Fallback for servers using HTTP_ prefix
    if (isset($_SERVER['HTTP_X_MRRLYTICS_KEY'])) {
        return $_SERVER['HTTP_X_MRRLYTICS_KEY'];
    }
    
    return null;
}

// ============================================================================
// MAIN EXECUTION FLOW
// ============================================================================

try {
    // 1. Verify HTTP method
    $requestMethod = isset($_SERVER['REQUEST_METHOD']) ? $_SERVER['REQUEST_METHOD'] : 'GET';
    if ($requestMethod !== 'GET') {
        sendErrorResponse(405, 'Method not allowed. Use GET.');
    }
    
    // 2. Get and validate API Key
    $providedKey = getAuthHeader();
    
    if (empty($providedKey)) {
        sendErrorResponse(401, 'Missing API key. Include X-MRRlytics-Key header.');
    }
    
    $storedKey = getAddonSetting('API_KEY');
    
    if (empty($storedKey)) {
        sendErrorResponse(500, 'API key not configured in WHMCS addon settings.');
    }
    
    // Constant-time comparison to prevent timing attacks
    if (!hash_equals($storedKey, $providedKey)) {
        sendErrorResponse(401, 'Invalid API key.');
    }
    
    // 3. Check rate limit
    $clientIp = getClientIp();
    $rateLimitSetting = getAddonSetting('RATE_LIMIT');
    $rateLimit = $rateLimitSetting ? (int) $rateLimitSetting : 300;
    $rateLimitResult = checkRateLimit($clientIp, $rateLimit);
    
    // Add rate limit headers
    header('X-RateLimit-Limit: ' . $rateLimit);
    header('X-RateLimit-Remaining: ' . $rateLimitResult['remaining']);
    
    if (!$rateLimitResult['allowed']) {
        header('Retry-After: ' . $rateLimitResult['retry_after']);
        sendErrorResponse(429, 'Rate limit exceeded. Try again later.', [
            'retry_after' => $rateLimitResult['retry_after'],
        ]);
    }
    
    // 4. Parse query parameters
    $limit = isset($_GET['limit']) ? (int) $_GET['limit'] : 1000;
    $offset = isset($_GET['offset']) ? (int) $_GET['offset'] : 0;
    $since = isset($_GET['since']) ? $_GET['since'] : null;
    
    // Validate limit (max 5000)
    $limit = max(1, min(5000, $limit));
    $offset = max(0, $offset);
    
    // Validate 'since' date format
    if ($since !== null) {
        $sinceTimestamp = strtotime($since);
        if ($sinceTimestamp === false) {
            sendErrorResponse(400, 'Invalid "since" parameter. Use ISO8601 format (e.g., 2024-01-01T00:00:00Z).');
        }
        $since = date('Y-m-d H:i:s', $sinceTimestamp);
    }
    
    // 5. Load and execute the data extractor
    require_once __DIR__ . '/lib/DataExtractor.php';
    
    $extractor = new \MRRlytics\DataExtractor($limit, $offset, $since, $debugMode);
    $result = $extractor->extract();
    
    // 6. Send successful response
    sendJsonResponse($result, 200);
    
} catch (\Exception $e) {
    // Handle any uncaught exceptions
    $extra = [];
    if ($debugMode) {
        $extra['file'] = $e->getFile();
        $extra['line'] = $e->getLine();
        $extra['trace'] = explode("\n", $e->getTraceAsString());
    }
    sendErrorResponse(500, 'Internal server error: ' . $e->getMessage(), $extra);
}
